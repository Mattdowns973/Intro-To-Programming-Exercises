# Can hash values be arrays? Can you have an array of hashes? (give examples)

=begin

Yes, hash values can be arrays.  I made it happen in the challege question
in the "Hashes" section when I took the original array of strings and split
them down into their separate characters.  Those made a hash where the key 
was the word, and the value was an array of single-character strings that
made up the word.  Yes you can have an array of hashes.  One example would
be if you could have an array for a baseball team.  Each separate element
of the array could be a hash containing all the characteristics for each
team member.
  
